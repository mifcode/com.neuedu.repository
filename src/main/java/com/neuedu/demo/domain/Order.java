package com.neuedu.demo.domain;

import java.io.Serializable;
import java.util.Date;

public class Order implements Serializable{
	private Integer orderid;
	private Integer customid;
	private String shopname;
	private Integer shopcount;
	private Integer total;
	private Date date;
	public Integer getOrderid() {
		return orderid;
	}
	public void setOrderid(Integer orderid) {
		this.orderid = orderid;
	}
	public Integer getCustomid() {
		return customid;
	}
	public void setCustomid(Integer customid) {
		this.customid = customid;
	}
	public String getShopname() {
		return shopname;
	}
	public void setShopname(String shopname) {
		this.shopname = shopname;
	}
	public Integer getShopcount() {
		return shopcount;
	}
	public void setShopcount(Integer shopcount) {
		this.shopcount = shopcount;
	}
	public Integer getTotal() {
		return total;
	}
	public void setTotal(Integer total) {
		this.total = total;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	@Override
	public String toString() {
		return "Order [orderid=" + orderid + ", customid=" + customid + ", shopname=" + shopname + ", shopcount="
				+ shopcount + ", total=" + total + ", date=" + date + "]";
	}
	public Order(Integer orderid, Integer customid, String shopname, Integer shopcount, Integer total, Date date) {
		super();
		this.orderid = orderid;
		this.customid = customid;
		this.shopname = shopname;
		this.shopcount = shopcount;
		this.total = total;
		this.date = date;
	}
	public Order() {
		super();
		// TODO Auto-generated constructor stub
	}
	
	
}
